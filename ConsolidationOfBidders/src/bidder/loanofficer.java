package bidder;

public class loanofficer {
	String ID;
	String password;
	public loanofficer(String iD, String password) {
		super();
		ID = iD;
		this.password = password;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	

}
